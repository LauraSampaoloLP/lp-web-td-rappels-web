    <h2>Biographie</h2>
    <article>
        En 2011, elle signe pour mettre en scène Thor : Le Monde des ténèbres, la suite du film Thor sorti
        cette même année. En raison de désaccords artistiques avec Marvel, elle est remerciée par la
        production, malgré l'insistance de Natalie Portman pour qu'elle reste à la réalisation du film.
        Quelques années plus tard, elle est finalement annoncée à la réalisation d'un autre film de
        super-héros, cette fois pour DC Comics et son univers cinématographique : Wonder Woman1.
